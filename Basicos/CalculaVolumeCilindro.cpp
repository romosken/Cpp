#include <iostream>
#include <math.h>
#include <cstdlib>

using namespace std;

float V=0, A=0, R=0;
const float P=3.14;

int main(){
    system("cls");
    setlocale(LC_ALL, "Portuguese");
    
    cout<< "Digite o raio da lata (em Cm) que deseja saber o volume!"<<endl;
    cin>> R;
    
    cout<< "Digite altura da lata (em Cm)que deseja saber o volume!"<<endl;
    cin>>A;
    
    V=pow(R,2)*P*A;
      
    cout<<"O volume da lata é de: "<<V<<"Cm³"<<endl;
    system("pause");
    return(0);
    
}
